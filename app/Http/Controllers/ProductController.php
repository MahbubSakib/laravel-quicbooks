     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $product_store = new Product;

        $product_store->product_name = $request->input('product_name');
        $product_store->category     = $request->input('category');
        $product_store->product_item = $request->input('product_item');

        $product_store->save();

        return response()->json($product_store);
    }

